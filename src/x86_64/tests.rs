    fn list_3_operand() {
        let mut total = 0;
        let mut vex = 0;
        let mut xop = 0;
        for c in Code::values() {
            if c.op_code().op_count() == 3 && c.op_code().is_instruction() {
                total = total + 1;
                if format!("{:?}", c).contains("VEX") {
                    vex = vex + 1;
                    continue;
                }
                if format!("{:?}", c).contains("XOP") {
                    xop = xop + 1;
                    continue;
                }
                println!(
                    "Code: {:?} => {:?}, {:?}, {:?}",
                    c,
                    c.op_code().op0_kind(),
                    c.op_code().op1_kind(),
                    c.op_code().op2_kind()
                )
            }
        }
        println!("Vex: {vex}");
        println!("Xop: {xop}");
        println!("Total: {total}");
        println!("Legacy: {}", total - (vex + xop));
    }

